    public void atualizar() {
        String mensagem = "";
        if(form2.operacao == 0){//inserir
            if(nulo()){//não inseriu nada
                mensagem = "Nenhum contato foi inserido!";
            }else{//inseriu
                try{
                    service.add(form2.contato);
                    contatos.add(form2.contato);
                    form2.contato = null;
                    mensagem = "Contato inserido com sucesso";
                }catch(RuntimeException e){
                    mensagem = e.getMessage();
                }
            }
        }
        if(form2.operacao == 1){//alterar
            if(nulo() || naoModificou()){//não alterou
                mensagem = "Nenhum contato foi alterado!";
            }else{
                try{
                    new ContatoService().update(form2.contato);
                    contatos.set(form2.pos,form2.contato);
                    form2.contato = null;
                    mensagem = "Contato alterado com sucesso!";
                }catch(RuntimeException e){
                    mensagem = e.getMessage();
                }
            }
        }
        JOptionPane.showMessageDialog(rootPane, mensagem);
        jList1.setListData(getIdNomeEmail().toArray());
    }
    
    private boolean nulo(){
        return form2.contato.getNome().equals("") &&
               form2.contato.getEmail().equals("") &&
               form2.contato.getEndereco().equals("") &&
               form2.contato.getComplemento().equals("") &&
               form2.contato.getCep().equals(-1) &&
               form2.contato.getCidade().equals("") &&
               form2.contato.getEstado().equals("") &&
               form2.contato.getEmailAlternativo().equals("");
    }
    
    private boolean naoModificou(){
        Contato cont = this.contatos.get(form2.pos);
        return cont.getNome().equals(form2.contato.getNome()) &&
               cont.getEmail().equals(form2.contato.getEmail()) &&
               cont.getEndereco().equals(form2.contato.getEndereco()) &&
               cont.getComplemento().equals(form2.contato.getComplemento()) &&
               cont.getCep().equals(form2.contato.getCep()) &&
               cont.getCidade().equals(form2.contato.getCidade()) &&
               cont.getEstado().equals(form2.contato.getEstado()) &&
               cont.getEmailAlternativo().equals(form2.contato.getEmailAlternativo());
    }
    
    private List<String> getIdNomeEmail(){
        List<String> resposta = new ArrayList<>();
        for(Contato cont:this.contatos){
            resposta.add(String.format("%s  %s", cont.getId(), cont.getNome().equals("") ? "Sem nome" : cont.getNome()));
        }
        return resposta;
    }
