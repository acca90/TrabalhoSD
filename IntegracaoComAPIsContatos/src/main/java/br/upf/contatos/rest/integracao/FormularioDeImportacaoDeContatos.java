    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        //editar contatos
        if(jList1.isSelectionEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Selecione algum contato para ser editado!");
        }else{
            Contato c = this.contatos.get(jList1.getSelectedIndex());
            Contato cont = new Contato();
            cont.setId(c.getId());
            cont.setCep(c.getCep());
            cont.setCidade(c.getCidade());
            cont.setComplemento(c.getComplemento());
            cont.setEmail(c.getEmail());
            cont.setEmailAlternativo(c.getEmailAlternativo());
            cont.setEndereco(c.getEndereco());
            cont.setEstado(c.getEstado());
            cont.setNome(c.getNome());
            form2.contato = cont;
            form2.operacao = 1;
            form2.pos = jList1.getSelectedIndex();
            form2.carregar();
            form2.setVisible(true);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

