    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        List<Contato> contacts = new ArrayList<>();
        switch (redeSocialSetada){
            case 1: 
                importarYahoo.secondaryYahooConnection(jTextField1.getText());
                importarYahoo.tratamentoYahooResposta();
                contacts.addAll(importarYahoo.getContatos());
                break;
            case 2: 
                importarTwitter.secondaryTwitterConnection(jTextField1.getText());
                importarTwitter.tratamentoTwitterResposta();
                contacts.addAll(importarTwitter.getContatos());
                break;
            case 3: 
                importarGmail.secondaryGmailConnection(jTextField1.getText());
                importarGmail.tratamentoGmailResposta();
                contacts.addAll(importarGmail.getContatos());
                break;
            case 4: 
                importarLinkedin.secondaryLinkedinConnection(jTextField1.getText());
                importarLinkedin.tratamentoLinkedinResposta();
                contacts.addAll(importarLinkedin.getContatos());
                break;
            case 5: 
                importarFacebook.secondaryFacebookConnection(jTextField1.getText());
                importarFacebook.tratamentoFacebookResposta();
                contacts.addAll(importarFacebook.getContatos());
                break;
        }
        contacts.removeAll(this.contatos);
        String mensagem = "";
        for(Contato contato:contacts){
            try{
                service.add(contato);
                this.contatos.add(contato);
            }catch(RuntimeException e){
                mensagem += "Nome: " + contato.getNome() + "  Email: " + contato.getEmail() + "\n";
            }
        }
        if(mensagem.equals("")){
            mensagem = "Todos os contatos foram inseridos com sucesso!";
        }else{
            mensagem = "Alguns contatos não puderam ser inseridos:\n" + mensagem;
        }
        JOptionPane.showMessageDialog(rootPane, mensagem);
        jList1.setListData(getIdNomeEmail().toArray());
        jTextField1.setText("");
    }//GEN-LAST:event_jButton1MouseClicked

